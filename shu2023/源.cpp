using namespace std;

int main()
{
	cin.sync_with_stdio(false);
	int t, b, ans, cnt;
	while (cin >> t)
	{
		cnt = 0;
		ans = 0;
		while (t--)
		{
			cin >> b;
			if (cnt == 0)
			{
				ans = b;
				cnt++;
			}
			else if(ans != b)
			{
				cnt--;
			}
			else
			{
				cnt++;
			}
		}
		cout << ans << endl;
	}
}